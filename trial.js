// Define the function to add two numbers
function addNumbers(num1, num2) {
    return num1 + num2;
}

// Call the function with different pairs of numbers and log the results
console.log("Sum of 5 and 10:", addNumbers(5, 10));
console.log("Sum of 20 and 30:", addNumbers(20, 30));
console.log("Sum of -5 and 15:", addNumbers(-5, 15));
console.log("Sum of 7.5 and 2.3:", addNumbers(7.5, 2.3));